public class ifElse {
    public static void main(String[] args) {

        int num = 9;
        if (num > 5 && num <= 9) {
            System.out.println("number between 5 and 9");
        } else if (num >= 1 && num <= 5) {
            System.out.println("number between 1 and 5");
        } else {
            System.out.println("INVALID");
        }

        int x = 5;
        int y = 5;
        if (x + y < 20) {
            System.out.println("less thanb 20");
        }
    }
}
